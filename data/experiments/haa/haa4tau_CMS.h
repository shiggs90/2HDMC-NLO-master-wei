double haa4tau_CMS(double x)
{
      //https://arxiv.org/abs/1907.07235
double limit = 1000000;
double SMXS=50.5764;
if ((4<=x)&&(x<=5)) {
      limit =0.232+(x-4)*(0.165-0.232)/(5-4);
}
if ((5<=x)&&(x<=6)) {
      limit =0.165+(x-5)*(0.125-0.165)/(6-5);
}
if ((6<=x)&&(x<=7)) {
      limit =0.125+(x-6)*(0.065-0.125)/(7-6);
}
if ((7<=x)&&(x<=8)) {
      limit =0.065+(x-7)*(0.038-0.065)/(8-7);
}
if ((8<=x)&&(x<=9)) {
      limit =0.038+(x-8)*(0.022-0.038)/(9-8);
}
if ((9<=x)&&(x<=10)) {
      limit =0.022+(x-9)*(0.025-0.022)/(10-9);
}
if ((10<=x)&&(x<=11)) {
      limit =0.025+(x-10)*(0.028-0.025)/(11-10);
}
if ((11<=x)&&(x<=12)) {
      limit =0.028+(x-11)*(0.036-0.028)/(12-11);
}
if ((12<=x)&&(x<=13)) {
      limit =0.036+(x-12)*(0.049-0.036)/(13-12);
}
if ((13<=x)&&(x<=14)) {
      limit =0.049+(x-13)*(0.076-0.049)/(14-13);
}
if ((14<=x)&&(x<=15)) {
      limit =0.076+(x-14)*(0.161-0.076)/(15-14);
}
return limit*SMXS;
}
